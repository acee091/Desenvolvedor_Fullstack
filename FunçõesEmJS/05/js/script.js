
// 5. Crie uma função chamada "imprimeNumeros" que recebe um parâmetro "num". A função deve imprimir no console todos os números de 0 até "num", em ordem crescente. 
// Em seguida, chame a função "imprimeNumeros" com diferentes valores para "num" e exiba o resultado no console.

function imprimeNumeros(num) {
    for (let i = 0; i <= num; i++){
        console.log(i);
    }
}
imprimeNumeros(10);
imprimeNumeros(5)